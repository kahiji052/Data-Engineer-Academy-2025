x = "10"         # String
y = int(x)       # Convierte a entero (10)
print(f'Conversión de string a int: {type(x)} - {type(y)}')

a = 5.7          # Float
b = str(a)       # Convierte a string ("5.7")
print(f'Conversión de float a string: {type(a)} - {type(b)}')

c = 0            # Entero
d = bool(c)      # Convierte a booleano (False)
print(f'Conversión de int a boolean: {type(c)} - {type(d)}')